public class People{
	String id;
	String name;
	boolean gender;
	String address;
	int age;
	
	People(String id,String name,boolean gender, String address,int age){
		this.id = id;
		this.name = name;
		this.gender = gender;
		this.address = address;
		this.age = age;
	}
	
}

